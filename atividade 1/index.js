let Valor = prompt("Digite um valor:)"); // Variável que armazena o valor escrito pelo usuário.

if(isNaN(Valor)) {
  alert("Valor inválido!");
} // Indica a invalidez de uma entrada de informação que não seja um número.

if(Valor >= 0) {
   alert("Valor positivo!");
} // Condição que avalia se o valor é maior ou igual a 0.

if(Valor <= 0) {
    alert("O Valor não é positivo!");
 } // Condição que avalia se o valor é maior ou menor a 0.
